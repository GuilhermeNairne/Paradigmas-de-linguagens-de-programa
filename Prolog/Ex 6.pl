6.Quais das próximas operações de unificação serão bem sucedidas e quias irão falhar? Para as que forem bem sucedidas, quais são as instanciações de variáveis resultantes?

a) ponto(A, B) = ponto(1, 2)
Bem-sucedida
Instanciação resultante: A = 1, B = 2

b) 2 + 2 = 4
Bem-sucedida
Nenhuma variável envolvida, resultado é verdadeiro.

c) ponto(A, B) = ponto(X, Y, Z)
Falha
O número de argumentos nos dois lados não é o mesmo.

d) mais(2, 2) = 4
Falha
Do lado esquerdo temos uma estrutura de termo (mais/2), enquanto do lado direito temos um número atômico (4).

e) +(2, D) = +(E, 2)
Bem-sucedida
Instanciação resultante: D = E.

f) t(p(-1,0), P2, P3) = t(P1, p(1, 0), p(0, Y))
Bem-sucedida
Instanciação resultante: P1 = p(-1, 0), P2 = p(1, 0), P3 = p(0, Y)